package com.example.common.enums;

/**
 * Enumeration representing different roles in the system.
 * Defines the access levels and permissions for each role.
 */
public enum RoleEnum {

    /**
     * Administrator role.
     * Typically has the highest level of access and permissions in the system.
     */
    ADMIN,

    /**
     * Merchant role.
     * Represents business entities managing goods or services in the system.
     */
    BUSINESS,

    /**
     * User role.
     * Represents regular users who interact with the system.
     */
    USER
}

